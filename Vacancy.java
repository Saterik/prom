package Home_Work_7;

public class Vacancy {
    private String companyName;
    private double salary;

    public Vacancy(String companyName, double salary2) {
        this.companyName = companyName;
        this.salary = salary2;
    }

    public String getCompanyName() {
        return companyName;
    }

    public double getSalary() {
        return salary;
    }
}
